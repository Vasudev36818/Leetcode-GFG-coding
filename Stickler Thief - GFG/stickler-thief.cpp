class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int rob1=0,rob2=0,temp=0;
        for(int i=0;i<n;i++){
            temp=max(rob1+arr[i],rob2);
            rob1=rob2;
            rob2=temp;
        }
        return rob2;
    }
};

